public class Numcheck {
    int num1;
    int num2;
    public Numcheck(int num1,int num2)
    {
        this.num1 = num1;
        this.num2 = num2;
    }
    int sum()
    {
        return num1+num2;
    }
    int prod()
    {
        return num1*num2;
    }
    int diff()
    {
        return num1-num2;
    }
    int div()
    {
        return num1/num2;
    }





}
